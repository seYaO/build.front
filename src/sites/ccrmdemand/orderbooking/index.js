    Index.getEndDate=function(date,num){
        var fDate = date.replace(/-/g,"/");
        var curDate = new Date(fDate);
        var curMonth = curDate.getMonth() + 1;

        curDate.setMonth(curMonth + num);
        curDate.setDate(0);
        var Y = curDate.getFullYear(),
            M = curDate.getMonth() + 1,
            D = curDate.getDate();
        return Y + "-" + M + "-" + D;
    };
    Index.getStartDate = function(curDate){
        var Y = curDate.getFullYear(),
            M = curDate.getMonth() + 1,
            D = curDate.getDate();
        return Y + "-" + M + "-" + D;
    };
    Index.calendar = function(){
        var startDate = Index.getStartDate(new Date());
        page.init();
        var fDate = ['元宵','清明','端午','七夕','中秋','重阳','情人','五一','儿童','国庆','圣诞'];

        $(".range_style").on("click",function(){
            var that = this;
            $.calendar({
                mode: "range",
                currentDate: $(that).attr("date-time").split(","),
                endDate:Index.getEndDate(startDate,11),
                tips:["请选择最早出游日期","请选择最晚出游日期"],
                dateVerify: function (dates) {
                    for (var i = 0; i < 2; i++) {
                        for (var j = 0; j < 3; j++) {
                            if (j == 0) continue;
                            var str = dates[i][j];
                            dates[i][j] = ("00"+str).slice(str.length);
                        }
                    }
                    var dateLength = new Date(dates[1].join("/")) - new Date(dates[0].join("/"));
                    if(dateLength <= 5184000000 && dates[1] >= dates[0]){
                        return true;
                    }
                    //return (dateLength <= 5184000000 && dateLength > 0);
                },
                fn: function (dates) {
                    for (var i = 0, len = dates.length; i < len; i++) {
                        var date = dates[i];
                        for (var j = 0, jLen = date.length; j < jLen; j++) {
                            if (date[j].length < 2) {
                                date[j] = "0" + date[j];
                            }
                        }
                    }
                    var html = dates[0].join("-")+","+dates[1].join("-");
                    $(that).attr("date-time", dates[0].join("-")+","+dates[1].join("-"));
                    $(that).val(html.replace(",", ","));
                    page.close();
                },
                rangeStyled: true,
                buildContent: function (date, dateStr, classArr, data) {
                    if(dateStr){
                        if(fDate.indexOf(dateStr) !== -1){
                            htmlStr = '<div>' + dateStr + '节</div>';
                        }else{
                            htmlStr = '<div>' + dateStr + '</div>';
                        }
                        return htmlStr;
                    }else{
                        return '<div>'+ date.getDate() +'</div>'
                    }
                }
            });
            page.open('calendar');
        });
    };
